;PROGRAM TO PRINT CAPITAL LETTERS FROM A-Z:
;__________________________________________

DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV CX,26
MOV BL,65
MOV DL,BL
LOOP1:
MOV AH,2
INT 21H
MOV DX,10
MOV AH,2
INT 21H
INC BL
MOV DL,BL
LOOP LOOP1
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
