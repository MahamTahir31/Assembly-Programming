; PROGRAM IN WHICH NESTED LOOP IS USED:
;______________________________________

DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
ARR DB "                         $"
.CODE
MAIN PROC       ;MAIN PROCEDURE STARTS HERE
MOV BX,1      ;TAKE REGISTER BX AND PUT VALUE 1
MOV CX,10       ;FIVE TIMES LOOP WILL EXECUTE
L1:             ;MAIN LOOP STARTS HERE
PUSH CX         ;PUSH COUNTER REGISTER
MOV CX,BX       ;MOVE BASE REGISTER TO COUNTER REGISTER
L2:             ;FIRST NESTED LOOP
MOV DX,'*'      ;DISPLAY PATTERNS
MOV AH,2
INT 21H

MOV CX,BX
LOO:
MOV DX,32
MOV AH,2
INT 21H
LOOP LOO
MOV DX,'*'
MOV AH,2
INT 21H
LOOP L2         ;FIRST NESTED LOOP ENDS HERE
INC BX
MOV DX,10
MOV AH,2
INT 21H          ;INCREMENT IN BASE REGISTER
POP CX          ;POP COUNTER REGISTER
PUSH CX         ;AGAIN PUSH COUNTER REGISTER
MOV CX,BX       ;MOVE BASE REGISTER TO COUNTER REGISTER
L3:             ;SECOND NESTED LOOP STARTS HERE
MOV DX,'*'      ;DISPLAY PATTERNS
MOV AH,2
INT 21H

LOOP L3         ;SECOND NESTED LOOP ENDS HERE
INC BX          ;INCREMENT IN BASE REGISTER
POP CX
MOV DX,10
MOV AH,2
INT 21H        ;POP COUNTER REGISTER
LOOP L1         ;MAIN LOOP ENDS HERE

MOV AH,4CH      ;EXITING REGISTER
INT 21H

MAIN ENDP       ;MAIN PROCEDURE ENDS HERE
END MAIN        ;END MAIN PROGRAM
