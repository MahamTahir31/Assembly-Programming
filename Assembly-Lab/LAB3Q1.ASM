; PROGRAM TO PRINT TWO STRINGS:
;______________________________

DOSSEG
.MODEL SMALL
.STACK 100H

.DATA
SENT1 DB 'MAHAM$'  ; STORE FIRST STRING TO A VARIABLE
SENT2 DB 'TAHIR$'  ; STORE SECOND STRING TO A VARIABLE

.CODE
MAIN PROC
MOV AX,@DATA       ; MOVE DEFINED VARIABLES TO ACCUMULATOR
MOV DS,AX          ; MOVE ACCUMULATOR TO DATA SEGMENT
LEA DX,SENT1       ; FOR DISPLAY OF FIRST STRING VARIABLE
MOV AH,9           
INT 21H                           
MOV DX,10          ; FOR NEWLINE FEED
MOV AH,2
INT 21H
MOV DX,13          ; FOR CARRIAGE RETURN
MOV AH,2
INT 21H
LEA DX,SENT2       ; FOR DISPLAY OF 2ND STRING VARIABLE
MOV AH,9
INT 21H
MOV AH,4CH         ; EXITING REGISTER
INT 21H
MAIN ENDP          ; MAIN PROCEDURE ENDS HERE

END MAIN           ; END MAIN PROGRAM
