;PROGRAM TO PRINT WHETHER THE NUMBER IS ODD OR EVEN:
;___________________________________________________
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA                    ;DECLARATION OF VARIABLES
INPUT DB 'ENTER A NUMBER : $'         
E DB 'NUMBER IS EVEN $'
O DB 'NUMBER IS ODD $'
.CODE
MAIN PROC               ; MAIN PROGRAM STARTS HERE
MOV AX,@DATA            ; MOVE .DATA STUFF TO REGISTER
MOV DS,AX
MOV DX,10
MOV AH,2
INT 21H
LEA DX,INPUT            ; DISPLAY INPUT MESSAGE
MOV AH,9
INT 21H
MOV AH,1                ; GETTING INTEGER INPUT FROM USER
INT 21H
MOV BL,2                ; DIVISOR
DIV BL                  ; DIVIDE INPUT NUMBER BY 2
CMP AH,0                ; COMPARE REMAINDER WITH 0

JNE L1                  ; JUMP WHEN REMAINDER NOT = 0
MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
LEA DX,E                ; DISPLAY WHEN REMAINDER = 0
MOV AH,9
INT 21H
MOV DX,10
MOV AH,2
INT 21H
MOV AH,4CH
INT 21H

L1:

MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
LEA DX,O                ; DISPLAY WHEN REMAINDER NOT = 0
MOV AH,9
INT 21H
MOV DX,10
MOV AH,2
INT 21H

MOV AH,4CH              ; EXITING REGISTER
INT 21H
MAIN ENDP               ; MAIN PROCEDURE ENDS HERE
END MAIN                ; END MAIN PROGRAM
