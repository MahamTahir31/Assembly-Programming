;PROGRAM FOR PROCEDURE:
;______________________
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA           ;VARIABLE DECLARATION
VAR1 DB 'THE WEATHER IS HOT TODAY :( $'
VAR2 DB 'BUT I LIKE WINTERS :) $'
VAR3 DB 'THEREFOR I AM NOT GOING OUTSIDE ANYMORE :-| $'
.CODE
MAIN PROC       ;MAIN PROCEDURE STARTS HERE
CALL NEW
MOV AX,@DATA    ;MOVING .DATA TO REGISTER
MOV DS,AX
LEA DX,VAR1     ;DISPLAY MESSAGE
MOV AH,9
INT 21H
CALL NEW        ;CALL PROCEDURE
LEA DX,VAR2     ;DISPLAY MESSAGE
MOV AH,9
INT 21H
CALL NEW        ;CALL PROCEDURE
LEA DX,VAR3     ;DISPLAY MESSAGE
MOV AH,9
INT 21H
CALL NEW        ;CALL PROCEDURE

MOV AH,4CH      ;EXITING REGISTER
INT 21H
MAIN ENDP       ;MAIN PROCEDURE ENDS HERE

NEW PROC        ;CREATING NEW PROCEDURE
MOV DX,10       ;FOR NEW LINE
MOV AH,2
INT 21H
MOV DX,13       ;FOR CARRIAGE RETURN
MOV AH,2
INT 21H
RET             ;RETURN ADDRESS OF DATA ON STACK
NEW ENDP

END MAIN        ;END MAIN PROGRAM

