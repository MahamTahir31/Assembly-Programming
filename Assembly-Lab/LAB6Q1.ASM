;PROGRAM TO SWAP TWO NUMBERS:
;____________________________

.MODEL SMALL
.STACK 100H
.DATA 
INPUT1 DB "ENTER 1ST NUMBER : $"
INPUT2 DB "ENTER 2ND NUMBER : $"
BEFORE DB "NUMBERS BEFORE SWAP: $" ; DECLARE VARIABLE 1
AFTER DB  "NUMBERS AFTER SWAP: $"  ; DECLARE VARIABLE 2
.CODE
MAIN PROC              
MOV AX,@DATA            
MOV DS,AX
LEA DX, INPUT1
MOV AH,9
INT 21H
MOV AH,1
INT 21H
MOV AX,AL
PUSH AX
LEA DX, INPUT2
MOV AH,9
INT 21H
MOV AH,1
INT 21H
MOV BX,AL
LEA DX,BEFORE
MOV AH,9
INT 21H                     
MOV DX,AX
MOV AH,2                
INT 21H
PUSH BX
MOV DX,BX
MOV AH,2
INT 21H
LEA DX, AFTER           
MOV AH,9
INT 21H
POP AX                
MOV DX,AX
MOV AH,2                
INT 21H
POP BX                  
MOV DX,BX
MOV AH,2                
INT 21H
MOV AH,4CH              
INT 21H
MAIN ENDP             
END MAIN                ; ENDING MAIN PROGRAM
