; DIVISION PROGRAM TO PRINT QUOTIENT & REMAINDER:
;________________________________________________

DOSSEG
.MODEL SMALL
.STACK 100H
.DATA                   ; DECLARATION OF VARIABLES
QUOTIENT DB 'QUOTIENT = $'
REMAINDER DB 'REMAINDER = $'
QUO DW ?
REM DW ?
.CODE
MAIN PROC               ; MAIN PROGRAM STARTS HERE
MOV AX,@DATA            ; TRANSFERING DATA OF .DATA TO REGISTER
MOV DS,AX
MOV AX,27               ; MOV DIVIDEND TO REGISTER
MOV BL,5                ; MOV DIVISOR TO REGISTER
DIV BL                  ; DIVIDE 27 FROM 5
MOV QUO,AL              ; MOV QUOTIENT FROM REGISTER TO VARIABLE
MOV REM,AH              ; MOV REMAINDER FROM REGISTER TO VARIABLE
LEA DX, QUOTIENT        ; DISPLAY MESSAGE
MOV AH,9
INT 21H
MOV DL,QUO              ; MOV QUOTIENT FROM VARIABLE TO REGISTER
ADD DL,48               ; MOV ASSCII OF 0 TO REGISTER FOR CONVERSION
MOV AH,2                ; DISPLAY QUOTIENT
INT 21H
MOV DX,10               ; FOR NEW LINE
MOV AH,2
INT 21H
LEA DX, REMAINDER       ; DISPLAY MESSAGE
MOV AH,9
INT 21H
MOV DL,REM              ; MOV REMAINDER FROM VARIABLE TO REGISTER
ADD DL,48               ; MOV ASSCII OF 0 TO REGISTER FOR CONVERSION
MOV AH,2                ; DISPLAY REMAINDER
INT 21H
MOV DX,10               ; FOR NEW LINE
MOV AH,2
INT 21H
MOV AH,4CH              ; EXITING REGISTER
INT 21H
MAIN ENDP               ; END MAIN PROCEDURE
END MAIN                ; MAIN PROGRAM ENDS HERE
