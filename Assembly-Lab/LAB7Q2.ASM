;PROGRAM TO INPUT STRING AND PRINT IT:
;_____________________________________
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA           ; DECLARING VARIABLES
ENT DB 'ENTER YOUR NAME:$'
ARR1 DB '________________$'
STR DB 100 DUP('$')
OUT DB ' ! YOU NEED TO CONCENTRATE ON YOUR STUDIES :) $'
ARR2 DB '__________________________________________________$'
.CODE
MAIN PROC
MOV AX,@DATA            ; MOVE .DATA TO REGISTER
MOV DS,AX                                                     
LEA DX,ENT              ; DISPLAY MESSAGE
MOV AH,9
INT 21H
MOV DX,10               ; FOR NEW LINE
MOV AH,2
INT 21H
MOV SI, OFFSET ARR1     ; FOR DISPLAYING 1ST ARRAY
MOV CX,16
L1:
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI
LOOP L1
MOV DX,10               ; FOR NEW LINE
MOV AH,2
INT 21H
MOV DX,10               ; FOR NEW LINE
MOV AH,2
INT 21H
MOV SI,OFFSET STR       ; PROCEDURE FOR STRING INPUT 
LOOP1:
MOV AH,1
INT 21H
CMP AL,13
JE PROGRAMEND
MOV [SI],AL
INC SI
JMP LOOP1
PROGRAMEND:
MOV DX,10               ; FOR NEW LINE               
MOV AH,2
INT 21H
MOV DX,OFFSET STR       ; DISPLAY STRING INPUT
MOV AH,9
INT 21H
LEA DX, OUT             ; DISPLAY MASSAGE
MOV AH,9
INT 21H
MOV DX,10               ; FOR NEW LINE
MOV AH,2
INT 21H
MOV SI, OFFSET ARR2     ; DISPLAY SECOND ARRAY
MOV CX,50
L2:
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI
LOOP L2

MOV AH,4CH              ; EXITING REGISTER
INT 21H
MAIN ENDP               ; END MAIN PROCEDURE
END MAIN                ; MAIN PROGRAM ENDS HERE
