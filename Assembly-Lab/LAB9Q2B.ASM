;PROGRAM FOR MACRO:
;__________________

DOSSEG

PRINT MACRO P1          ;CREATING MACRO METHOD 
LEA DX,P1
MOV AH,9
INT 21H
ENDM

.MODEL SMALL
.STACK 100H
.DATA                   ;DECLARATION OF VARIBALES
VAR1 DB  " ! WELCOME TO THE ASSEMBLY WORLD $"
INPUT DB 100 DUP('$')
.CODE
MAIN PROC               ;MAIN PROCEDURE STARTS HERE
MOV AX,@DATA            ;MOVE .DATA TO REGISTER
MOV DS,AX
MOV SI,OFFSET INPUT     ;TRANSFER ADDRESS OF INPUT TO SOURCE INDEX
L1:                     ;BEGINING OF FIRST LOOP
MOV AH,1                ;TAKING INPUT
INT 21H
CMP AL,13               ;COMPARE INPUT BY SERVICE ROUTINE 13
JE PROGRAMEND           ;JUMP WHEN INPUT IS EQUAL TO 13
MOV [SI],AL
INC SI
JMP L1
PROGRAMEND:             ;JUMP CONDITION ENDS HERE
MOV DX,10               ;FOR NEW LINE
MOV AH,2
INT 21H
PRINT INPUT
PRINT VAR1              ;DISPLAY VAR1 BY USING MACRO

MOV AH,4CH              ;EXITING REGISTER
INT 21H
MAIN ENDP               ;MAIN PROCEDURE ENDS HERE
END MAIN                ;END MAIN PROGRAM
