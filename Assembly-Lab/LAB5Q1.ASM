; PROGRAM TO PRINT THE INPUT NUMBER IS EQUAL OR NOT TO GIVEN NUMBER:
;___________________________________________________________________

DOSSEG                       
.MODEL SMALL
.STACK 100H
.DATA
INPUT DB "ENTER ANY NUMBER: $"
SENT1 DB "NUMBER IS EQUAL $"
SENT2 DB "NUMBER IS NOT EQUAL $"                                     
.CODE
MAIN PROC       ; MAIN PROCEDURE STARTS HERE
MOV AX,@DATA    ; MOVING .DATA TO ACCUMULATOR 
MOV DS,AX       ; MOVING DATA OF ACCUMULATOR TP DATA REGISTER
LEA DX,INPUT    ; ASKING FOR ENTER ANY NUMBER
MOV AH,9        ; DISPLAY ABOVE COMMAND
INT 21H

MOV DL,'3'      ; COMPARE NUMBER BY 3
MOV AH,1        ; NUMBER FROM WHICH TO COMPARE
INT 21H
CMP AL,DL       ; COMPARING TWO NUMBERS
JE L1           ; JUMP CONDTIONS BLOCK STARTS HERE
MOV DX,10       ; FOR NEW LINE
INT 21H
LEA DX,SENT2    ; DISPLAY SENT2 DATA
MOV AH,9
INT 21H
MOV AH,4CH      ; EXITING REGISTER
INT 21H
L1:
MOV DX,10       ; FOR NEW LINE
INT 21H
LEA DX,SENT1    ; DISPLAY SENT1 DATA
MOV AH,9
INT 21H
MOV AH,4CH      ; EXITING REGISTER
INT 21H
MAIN ENDP       ; MAIN PROCEDURE ENDS HERE
END MAIN        ; END MAIN PROGRAM
