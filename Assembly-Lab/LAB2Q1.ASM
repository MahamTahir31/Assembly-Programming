; PROGRAM TO ADD TWO NUMBERS:
;____________________________

.MODEL SMALL
.STACK 100H
.DATA
NUM1 DB "ENTER 1ST NUMBER : $"
NUM2 DB "ENTER 2ND NUMBER : $"
RES DB "THE SUM OF TWO NUMBERS = $"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX, NUM1
MOV AH,9
INT 21H
MOV AH,1
INT 21H
MOV BL,AL
MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
LEA DX,NUM2
MOV AH,9
INT 21H
MOV AH,1
INT 21H
ADD BL,AL
MOV DX,10
MOV AH,2
INT 21H
LEA DX, RES
MOV AH,9
INT 21H
SUB BL,48
MOV DL,BL
MOV AH,2
INT 21H
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

; WARNING : ENTER NUMBERS FROM 1 TO 9 WHICH CONTAINS THE SUM<10
