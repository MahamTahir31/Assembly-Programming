;PROGRAM TO REVERSE A STRING:
;____________________________

.MODEL SMALL
.STACK 100H
.DATA
BEFORE DB "WORD BEFORE REVERTED: $"
AFTER DB "WORD AFTER REVERTED: $"
STRING DB "ROBOT$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX, BEFORE
MOV AH,9
INT 21H
MOV DX,10
MOV AH,2
INT 21H
LEA DX, STRING
MOV AH,9
INT 21H
LEA SI, STRING

MOV CX,5
L1:
MOV BX,[SI]
PUSH BX
INC SI
LOOP L1
MOV DX,10
MOV AH,2
INT 21H
LEA DX, AFTER
MOV AH,9
INT 21H
MOV DX,10
MOV AH,2
INT 21H

MOV CX,5
LOOP2:
POP BX
MOV DX,BX
MOV AH,2
INT 21H
LOOP LOOP2

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
