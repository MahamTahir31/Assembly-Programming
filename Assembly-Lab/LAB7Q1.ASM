;PROGRAM TO PRINT AN ARRAY USING LOOP:
;_____________________________________
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
ARRAY1 DB 'FROM UBIT I BEGAN MY BSCS JOURNEY$'
ARRAY2 DB '__________________________________$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET ARRAY1
MOV CX,33
LOOP1:
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI
LOOP LOOP1
MOV DX,10
MOV AH,2
INT 21H
MOV SI,OFFSET ARRAY2
MOV CX,34
LOOP2:
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI
LOOP LOOP2

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
